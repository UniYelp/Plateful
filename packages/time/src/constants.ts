/**
 * @unit ms
 */
export const SECOND = 1000;

/**
 * @unit ms
 */
export const MINUTE = SECOND * 60;

/**
 * @unit ms
 */
export const HOUR = MINUTE * 60;

/**
 * @unit ms
 */
export const DAY = HOUR * 24;

export const DAYS_IN_WEEK = 7;

/**
 * @unit ms
 */
export const WEEK = DAY * DAYS_IN_WEEK;
